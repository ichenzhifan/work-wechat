              <el-button size="mini" type="text" @click="visible = false">取消</el-button>
              <el-button type="primary" size="mini" @click="visible = false">确定</el-button>
            </div>
            <el-button
              slot="reference"
              type="primary"
              icon="el-icon-plus"
              size="mini"
              @click="handleAdd"
            >添加分类</el-button>
          </el-popover>
        </div>
        <div class="head-container">
          <el-tree
            :data="deptOptions"
            :props="defaultProps"
            :expand-on-click-node="false"
            :filter-node-method="filterNode"
            ref="tree"
            default-expand-all
            @node-click="handleNodeClick"
          />
        </div>
      </el-col>
      <!--用户数据-->
      <el-col :span="20" :xs="24">
        <el-checkbox
          :indeterminate="isIndeterminate"
          v-model="checkAll"
          @change="handleCheckAllChange"
        >全选</el-checkbox>
        <el-button type="primary" icon="el-icon-plus" size="mini" @click="handleAdd">添加</el-button>
        <el-popover placement="top" width="160" v-model="visible">
          <div>选择分组</div>
          <el-select v-model="model" size="small" placeholder>
            <el-option
              v-for="item in options"
              :key="item.value"
              :label="item.label"
              :value="item.value"
            ></el-option>
          </el-select>
          <div style="text-align: right; margin: 0">
            <el-button size="mini" type="text" @click="visible = false">取消</el-button>
            <el-button type="primary" size="mini" @click="visible = false">确定</el-button>
          </div>
          <el-button slot="reference">移动分组</el-button>
        </el-popover>
        <el-button type="primary" icon="el-icon-plus" size="mini" @click="handleAdd">删除</el-button>

        <el-row :gutter="20">
          <el-col :span="6" style="margin-top: 24px;" v-for="(item, index) in 10" :key="index">
            <el-card shadow="hover" body-style="padding: 0px;">
